const LANG_FONTS = {
  lo: "'Noto Sans Lao', sans-serif",
  th: "'Noto Sans Thai', sans-serif",
  en: "'Segoe UI', system-ui, -apple-system, sans-serif",
};

function getCurrentLang() {
  return localStorage.getItem('selectedLanguage') || 'en';
}

function t(key) {
  const lang = getCurrentLang();
  const dict = TRANSLATIONS[lang] || TRANSLATIONS.en;
  return dict[key] !== undefined ? dict[key] : (TRANSLATIONS.en[key] !== undefined ? TRANSLATIONS.en[key] : key);
}

function applyTranslations(lang) {
  const t = TRANSLATIONS[lang] || TRANSLATIONS.en;

  // text content — preserves child elements (badges, asterisks, etc.)
  // by only swapping the first text node when the element has element children.
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] === undefined) return;
    if (el.children.length === 0) {
      el.textContent = t[key];
      return;
    }
    // Has child elements — replace the first non-empty text node, blank the rest.
    let replaced = false;
    for (const node of Array.from(el.childNodes)) {
      if (node.nodeType !== Node.TEXT_NODE) continue;
      if (!replaced && node.textContent.trim() !== '') {
        node.textContent = t[key] + ' ';
        replaced = true;
      } else if (replaced) {
        node.textContent = '';
      }
    }
    if (!replaced) el.insertBefore(document.createTextNode(t[key] + ' '), el.firstChild);
  });

  // innerHTML (for keys with HTML like hero_title, footer)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  // placeholders
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  // font switch
  document.body.style.fontFamily = LANG_FONTS[lang] || LANG_FONTS.en;

  // update <html lang>
  document.documentElement.lang = lang === 'lo' ? 'lo' : lang === 'th' ? 'th' : 'en';
}
